package com.example.Application17.Model;

public class CommonData {
    private int id;
    private String data;
    public CommonData() {
    }
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getData() {
        return data;
    }
    public void setData(String data) {
        this.data = data;
    }
    public CommonData(int id, String data) {
        this.id = id;
        this.data = data;
    }
    
}
